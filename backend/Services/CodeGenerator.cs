        sb.AppendLine($"using MegaCrit.Sts2.Core.Commands;");
        sb.AppendLine($"using MegaCrit.Sts2.Core.Entities.Creatures;");
        sb.AppendLine($"using MegaCrit.Sts2.Core.Entities.Players;");
        sb.AppendLine($"using MegaCrit.Sts2.Core.Runs;");
        sb.AppendLine($"using MegaCrit.Sts2.Core.Combat;");
        sb.AppendLine($"using HarmonyLib;");
        sb.AppendLine($"using System.Collections.Generic;");
        sb.AppendLine();

        // Mod entry point
        sb.AppendLine($"namespace {ns};");
        sb.AppendLine();
        sb.AppendLine($"[ModInitializer(\"Initialize\")]");
        sb.AppendLine($"public static class {className}Mod");
        sb.AppendLine("{");
        sb.AppendLine($"    private static Harmony? _harmony;");
        sb.AppendLine();
        sb.AppendLine($"    public static void Initialize()");
        sb.AppendLine($"    {{");
        sb.AppendLine($"        _harmony = new Harmony(\"com.cardforge.{card.Id}\");");
        sb.AppendLine($"        _harmony.PatchAll(typeof({className}Mod).Assembly);");
        sb.AppendLine($"        Log.Warn(\"[{card.Name}] Card Forge mod loaded\");");
        sb.AppendLine($"    }}");
        sb.AppendLine("}");
        sb.AppendLine();

        // Card class
        sb.AppendLine($"public class {className}Card : CardModel");
        sb.AppendLine("{");
        sb.AppendLine($"    public override LocString Title => \"{card.Name}\";");
        sb.AppendLine($"    public override string Id => new ModelId(\"CARD_{card.Id.ToUpper()}\");");
        sb.AppendLine($"    public override CardRarity Rarity => CardRarity.{card.Rarity};");
        sb.AppendLine($"    public override CardType CardType => CardType.{card.Type};");
        sb.AppendLine();

        // Cost
        if (card.UseStarCost)
        {
            sb.AppendLine($"    public override int StarCost => {card.Cost};");
        }
        else
        {
            sb.AppendLine($"    public override int EnergyCost => {card.Cost};");
        }
        sb.AppendLine();

        // Tags
        GenerateTags(sb, card);

        // Play effect
        sb.AppendLine($"    public override void OnPlay(Creature target, Creature source, object? context)");
        sb.AppendLine($"    {{");
        GenerateEffects(sb, card);
        sb.AppendLine($"    }}");
        sb.AppendLine();

        // Upgrades
        GenerateUpgrades(sb, card);

        sb.AppendLine("}");

        return sb.ToString();
    }

    private void GenerateTags(StringBuilder sb, CardDefinition card)
    {
        if (card.Tags.Contains("Exhaust")) sb.AppendLine("    public override bool Exhaust => true;");
        if (card.Tags.Contains("Ethereal")) sb.AppendLine("    public override bool Ethereal => true;");
        if (card.Tags.Contains("Innate")) sb.AppendLine("    public override bool Innate => true;");
        if (card.Tags.Contains("Retain")) sb.AppendLine("    public override bool Retain => true;");
        if (card.Tags.Contains("Eternal")) sb.AppendLine("    public override bool Eternal => true;");
        if (card.Tags.Contains("Sly")) sb.AppendLine("    public override bool Sly => true; // 奇巧：弃掉时免费打出");
        if (card.Tags.Contains("Replay")) sb.AppendLine("    public override bool Replay => true; // 重放：额外打出一次");
        if (card.Tags.Contains("Unplayable")) sb.AppendLine("    public override bool Unplayable => true;");
        if (card.Tags.Count > 0) sb.AppendLine();
    }

