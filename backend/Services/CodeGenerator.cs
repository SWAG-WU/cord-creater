{
    public string Generate(CardDefinition card)
    {
        var sb = new StringBuilder();
        var className = ToPascalCase(card.Id);
        var ns = $"CardForge_{className}";

        sb.AppendLine("// Auto-generated by Card Forge");
        sb.AppendLine($"using MegaCrit.Sts2.Core.Modding;");
