    private void GenerateUpgrades(StringBuilder sb, CardDefinition card)
    {
        var upgrade = card.Upgrade;
        if (upgrade.Levels.Count == 0 && upgrade.AutoRule == null) return;

        sb.AppendLine("    public override void ApplyUpgrade(int level)");
        sb.AppendLine("    {");

        if (upgrade.Mode == "auto" && upgrade.AutoRule != null)
        {
            var field = Capitalize(upgrade.AutoRule.Field);
            var delta = upgrade.AutoRule.Value;
            sb.AppendLine($"        base.{field} += {delta} * level;");
        }
        else
        {
            for (int i = 0; i < upgrade.Levels.Count; i++)
            {
                var level = upgrade.Levels[i];
                var condition = i == 0 ? "if" : "else if";
                sb.AppendLine($"        {condition} (level == {i + 1})");
                sb.AppendLine("        {");
                foreach (var change in level.Changes)
                {
                    if (change.Type == "numeric")
                    {
                        var field = Capitalize(change.Field ?? "damage");
                        sb.AppendLine($"            base.{field} += {change.Value};");
                    }
                    else if (change.Type == "keyword")
                    {
                        if (change.Action == "add")
                            sb.AppendLine($"            AddKeyword(CardKeyword.{change.Keyword});");
                        else if (change.Action == "remove")
                            sb.AppendLine($"            RemoveKeyword(CardKeyword.{change.Keyword});");
                    }
                }
                sb.AppendLine("        }");
            }
        }

        sb.AppendLine("    }");
        sb.AppendLine();
    }

    private static string ToPascalCase(string input)
    {
        var parts = input.Split('_', ' ', '-');
        return string.Concat(parts.Select(p => char.ToUpper(p[0]) + p[1..].ToLower()));
    }

    private static string Capitalize(string s) => char.ToUpper(s[0]) + s[1..].ToLower();
}
